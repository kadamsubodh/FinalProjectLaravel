     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $validate=$request->validate([
            "subscriberEmail"=>"required|email",
        ]);
   
      if(! Newsletter::isSubscribed($request->subscriberEmail))
      {
        Newsletter::subscribe($request->subscriberEmail);        
        Session::flash('alert-success', 'Thank you for your subscription!');
        return redirect()->back();
      }
      else
      { 
        Session::flash('alert-success', 'You are already subscribed!');
        return redirect()->back();

      }

    }

